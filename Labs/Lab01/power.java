import java.util.Scanner;

class Power {
    public static void main(String[] args) {
        System.out.println((long) Math.pow(2, 31));
    }
}

/*
1. Java is statically typed, so variable types (int, long) must be declared,
   while Python is dynamically typed and does not require explicit type declarations.

2. Java requires a class definition and a main method as the program entry point,
   whereas Python code can run directly at the top level of the file.

3. Java has fixed-size integer types which can overflow,
   while Python integers automatically grow to arbitrary size.
*/